package com.jmethods.catatumbo;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Specifies the ID of an entity. Identifiers uniquely identify a given entity within the Cloud
 * Datastore. Cloud Datastore supports Integer (64-bit) and String types for identifiers, therefore
 * this annotation is only valid for the following field types:
 *
 * <ul>
 *   <li>Primitive long (long)
 *   <li>Long Wrapper (java.lang.Long)
 *   <li>String (java.lang.String)
 *   <li>Any other type that satisfies the following criteria:
 *       <ul>
 *         <li>Has a public <code>getValue</code> method with no parameters and a return type of
 *             long, Long or String
 *         <li>Has a public constructor with a single parameter whose type is same as the return
 *             type of <code>getValue</code> method.
 *       </ul>
 * </ul>
 *
 * <p>Note that Identifier fields cannot have a custom name in the Cloud Datastore. The Datastore
 * names them wither Id or Name depending on the data type long or String.
 *
 * @author Sai Pullabhotla
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.FIELD)
public @interface Identifier {

  /**
   * Specifies if identifiers should be generated automatically when inserting new entities into the
   * Cloud Datastore. When <code>autoGenerated</code> is set to <code>true</code> -
   *
   * <ul>
   *   <li>If the data type is long or Long - the Identifier generation is delegated to the Cloud
   *       Datastore. In other words, the Cloud Datastore allocates the identifiers.
   *   <li>If the data type is String - the framework generates the identifier using a random UUID.
   * </ul>
   *
   * When <code>autoGenerated</code> is set to <code>false</code>, the application is responsible
   * for managing the identifiers.
   *
   * @return whether or not to auto generate identifiers
   */
  boolean autoGenerated() default true;
}
